    private boolean verificaValorCompra() {

        double somaParcelas = 0.00;
        double valorCompra = Double.parseDouble(tfValorCompra.getText().replace(",", "."));

        if (!titulos.isEmpty()) {
            for (TituloCompra tv : titulos) {
                somaParcelas += tv.getValorParcela();
            }
        }

        return somaParcelas != valorCompra;
    }

