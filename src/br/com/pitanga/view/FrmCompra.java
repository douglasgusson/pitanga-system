    private void calculaValorItem() {
        double quantidade = Double.parseDouble(this.tfQuantidade.getText().replace(",", "."));
        double valorUnitario = Double.parseDouble(this.tfValorCompra.getText().replace(",", "."));
        double desconto = Double.parseDouble(this.tfDesconto.getText().replace(",", "."));

        double valorItem = (quantidade * valorUnitario) - ((quantidade * valorUnitario) * (desconto / 100));

        this.tfValorItem.setText(new DecimalFormat("###0.00").format(valorItem));
    }

    private void adicionarItem(double desconto, double valor, double quantidade, Produto produto) {
        ItemCompra item = new ItemCompra();
        item.setProduto(produto);
        item.setQuantidade(quantidade);
        item.setValor(valor);
        item.setDesconto(desconto);
        this.itens.add(item);
        this.tbItemCompra.setModel(new ItemCompraTableModel(itens));

        double subDesconto = 0.00;
        double subTotal = 0.00;
        for (ItemCompra iv : itens) {
            subDesconto += ((iv.getQuantidade() * iv.getValor()) * (iv.getDesconto() / 100));
            subTotal += ((iv.getValor() * iv.getQuantidade()) - ((iv.getValor() * iv.getQuantidade()) * (iv.getDesconto() / 100)));
        }

        StringUtils strUtil = new StringUtils();

        this.tfSubDesconto.setText(strUtil.formatarDecimalTabela(subDesconto));
        this.tfSubTotal.setText(strUtil.formatarDecimalTabela(subTotal));
        this.tfCodigoProduto.setText(null);
        this.tfDescricao.setText(null);
        this.tfQuantidade.setText("0,00");
        this.tfValorCompra.setText("0,00");
        this.tfDesconto.setText("0,00");
        this.tfValorItem.setText("0,00");
        this.tfCodigoProduto.requestFocus();
    }

