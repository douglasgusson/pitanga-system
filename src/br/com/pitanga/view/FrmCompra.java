    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        if (this.tfCodigoFornecedor.getText().isEmpty() || this.tfCodigoFornecedor.getText().equals("00000")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, informe o fornecedor.");
            this.tfCodigoFornecedor.requestFocus();
        } else if (this.itens.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Lance pelo menos um item.");
        } else if (titulos.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe pelo menos uma parcela.");
            btParcelamento.requestFocus();
        } else if (verificaValorCompra()) {
            try {
                gravarCompra();
                try {
                    gravarItem();
                    
                } catch (SQLException ex) {
                    Logger.getLogger(FrmCompra.class.getName()).log(Level.SEVERE, null, ex);
                }
                gravarTitulo();
                alterarQtdProduto();
                atualizarCompra();
            } catch (ParseException ex) {
                Logger.getLogger(FrmCompra.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btGravarActionPerformed

