    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        try {
            String host = tfHost.getText();
            String porta = tfPorta.getText();
            String endereco = tfEndereco.getText();
            String senha = tfSenha.getText();

            Email email = new Email(host, porta, endereco, senha);

            File xmlMap = new File("email.xml");
            OutputStream streamOut = null;

            if (!(xmlMap.exists())) {
                xmlMap.createNewFile();
            } else {
                xmlMap.delete();
                xmlMap.createNewFile();
            }

            streamOut = new FileOutputStream(xmlMap);
            XStream xstream = new XStream(new DomDriver());
            xstream.toXML(email, streamOut);

            JOptionPane.showMessageDialog(null, "Registro salvo!");

            inicializarFrame();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmConfiguraEmail.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmConfiguraEmail.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btGravarActionPerformed

