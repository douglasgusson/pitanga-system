    private void inicializarFrame() {

        this.setTitle("Configurações de E-mail");
        GUIUtils.considerarEnterComoTab(this);

        btGravar.setEnabled(false);
        tfHost.setEnabled(false);
        tfPorta.setEnabled(false);
        tfEndereco.setEnabled(false);
        tfSenha.setEnabled(false);

        Email email = getEmail();

        tfHost.setText(email.getHost());
        tfPorta.setText(email.getPorta());
        tfEndereco.setText(email.getEndereco());
        tfSenha.setText(email.getSenha());

    }

    private Email getEmail() {
        try {
            StringBuilder xml = new StringBuilder();
            Scanner scanner = new Scanner(new FileReader("email.xml"));
            while (scanner.hasNext()) {
                xml.append(scanner.next());
            }

            XStream xStream = new XStream(new DomDriver());
            Email email = (Email) xStream.fromXML(xml.toString());

            return email;

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null,
                    "Arquivo email.xml não encontrado.",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }

