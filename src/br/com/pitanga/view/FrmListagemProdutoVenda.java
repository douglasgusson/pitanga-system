    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        RelatorioDAO obj = DAOFactory.getDefaultDAOFactory().getRelatorioDAO();

        Calendar dataInicial = tfDataInicio.getCalendar();
        Calendar dataFinal = tfDataFim.getCalendar();

        JasperViewer jv = obj.gerarRelatorioVendaProduto(produto, dataInicial, dataFinal);

        FrmReport frmReport = new FrmReport(this, "tetettete", true, jv);
        frmReport.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    public void setProduto(Produto produto) {
        this.tfCodigoProduto.setText(String.format("%05d", produto.getIdProduto()));
        this.tfDescricao.setText(produto.getDescricao());
    }

