    private void tfCodigoProdutoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfCodigoProdutoFocusLost
        if (!tfCodigoProduto.getText().isEmpty()) {
            ProdutoDAO pd = DAOFactory.getDefaultDAOFactory().getProdutoDAO();
            produto = pd.buscar(Integer.parseInt(this.tfCodigoProduto.getText()));
            setProduto(produto);
        } else if ((this.tfCodigoProduto.getText().trim()).equals("00000")) {
            FrmBuscaProduto frmBuscaProduto = new FrmBuscaProduto(this, this);
            frmBuscaProduto.setVisible(true);
        }
    }//GEN-LAST:event_tfCodigoProdutoFocusLost

