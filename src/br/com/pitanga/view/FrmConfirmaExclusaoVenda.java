    private void setVenda(Venda v) {
        tfCodigoVenda.setText(StringUtils.inteiroCincoDig(v.getIdVenda()));
        tfDataVenda.setDate(v.getDataVenda());
        tfValor.setText(StringUtils.formatarDecimal(v.getValorVenda()));
        tfNomeCliente.setText(v.getCliente().getNome());

        ItemVendaDAO itemVendaDAO = DAOFactory.getDefaultDAOFactory().getItemVendaDAO();
        TituloVendaDAO tituloVendaDAO = DAOFactory.getDefaultDAOFactory().getTituloVendaDAO();
        itens = itemVendaDAO.listarPorVenda(venda.getIdVenda());
        tbItemVenda.setModel(new ItemVendaTableModel(itens));
        titulos = tituloVendaDAO.listarPorVenda(venda.getIdVenda());
        tbTituloVenda.setModel(new TituloVendaTableModel(titulos));
    }

