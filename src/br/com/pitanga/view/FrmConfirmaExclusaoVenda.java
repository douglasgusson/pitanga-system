    private void btSimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimActionPerformed
        int i = JOptionPane.showConfirmDialog(null,
                "Deseja mesmo excluir?\n\n"
                + "Atenção: esta ação removerá também todos os títulos \n"
                + "e todos os itens associados a essa venda.",
                "Excluir", JOptionPane.YES_NO_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            VendaDAO vendaDAO = DAOFactory.getDefaultDAOFactory().getVendaDAO();
            if (vendaDAO.remover(venda.getIdVenda())) {
                JOptionPane.showMessageDialog(null, "Venda removida!");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btSimActionPerformed

    private void initialize() {
        this.setTitle("Pitanga System - Confirmação de Exclusão");
        GUIUtils.considerarEnterComoTab(this);
        // Se teclarmos ESC nesta janela, ela irá se fechar:  
        this.getRootPane().registerKeyboardAction((ActionEvent e) -> {
            FrmConfirmaExclusaoVenda.this.dispose();
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0),
                JComponent.WHEN_IN_FOCUSED_WINDOW);
    }

