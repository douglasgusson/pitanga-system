    private void btCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcularActionPerformed
        // TODO add your handling code here:
        if (!this.titulos.isEmpty()) {
            this.titulos.removeAll(titulos);
            this.tbTituloCompra.setModel(new TituloCompraTableModel(titulos));
            int quantParcelas = Integer.parseInt(this.tfQuantParcelas.getText());
            int prazo = Integer.parseInt(this.tfPrazo.getText());
            lancarParcelas(quantParcelas, prazo);
        } else {
            int quantParcelas = Integer.parseInt(this.tfQuantParcelas.getText());
            int prazo = Integer.parseInt(this.tfPrazo.getText());
            lancarParcelas(quantParcelas, prazo);
        }
    }//GEN-LAST:event_btCalcularActionPerformed

    private void inicialiarFrame() {

        this.tfValor.setEnabled(false);
        this.tfVencimento.setEnabled(false);
        this.btAlterar.setEnabled(false);

        this.tbTituloCompra.setNextFocusableComponent(this.btOk);
        // Seta o título para o frame
        this.setTitle("Pitanga System - Títulos");
        // Instancia a classe Complemento
        GUIUtils obj = new GUIUtils();
        // Habilita o Enter como TAB, passando o Componente
        obj.considerarEnterComoTab(this);
    }

    private void lancarParcelas(int quantParcela, int prazo) {

        double valorParcela = (valorVenda / quantParcela);

        Calendar c = Calendar.getInstance();
        c.setTime(dataVenda);

        for (int i = 0; i < quantParcela; i++) {

            c.add(Calendar.DATE, +prazo);
            Date vencimento = c.getTime();

            TituloCompra titulo = new TituloCompra();
            titulo.setNumParcela(i + 1);
            titulo.setValorParcela(valorParcela);
            titulo.setVencimento(vencimento);

            titulos.add(titulo);

        }

        this.tbTituloCompra.setModel(new TituloCompraTableModel(titulos));

    }

