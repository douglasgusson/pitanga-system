    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed

        if (!this.titulos.isEmpty()) {
            if (this.titulos.size() > 1) {
                this.parcelamento.setText(this.titulos.size() + " PARCELAS");
                this.quantParcela.setText("" + this.titulos.size());
            } else {
                this.parcelamento.setText(this.titulos.size() + " PARCELA");
                this.quantParcela.setText("" + this.titulos.size());
            }
        } else {
            this.parcelamento.setText("Nenhuma parcela lançada.");
        }
        this.dispose();
    }//GEN-LAST:event_btOkActionPerformed

